    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //改变房间类型
        String rno=jTextField3.getText();
        if(rno.length()<=0){
            jDialog3.setVisible(true);
        }
        else{
        int rt_no=0;
        String rt=jComboBox2.getSelectedItem().toString();
        switch(rt){
            case "单人间":
                rt_no=1;
                break;
            case "双人间":
                rt_no=2;
                break;
            case "三人间":
                rt_no=3;
                break;
            case "豪华单人间":
                rt_no=4;
                break;
            case "豪华双人间":
                rt_no=5;
                break;
            case "豪华三人间":
                rt_no=6;
                break;
            case "总统套房":
                rt_no=7;
                break;
        }
        DBUtil db=new DBUtil();
        db.Connection();
        db.changeRoomType(rno, rt_no);
        jDialog1.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

