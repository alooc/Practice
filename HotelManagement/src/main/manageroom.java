    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rno=jTextField1.getText();
        String rtel=jTextField2.getText();
        if(rno.length()<=0||rtel.length()<+0){
            if(rno.length()<+0){
                jDialog3.setVisible(true);
            }
            else{
                if(rtel.length()<=0){
                    jDialog4.setVisible(true);
                }
            }
        }
        else{
        int rt_no=0;
        String rt=jComboBox1.getSelectedItem().toString();
        switch(rt){
            case "单人间":
                rt_no=1;
                break;
            case "双人间":
                rt_no=2;
                break;
            case "三人间":
                rt_no=3;
                break;
            case "豪华单人间":
                rt_no=4;
                break;
            case "豪华双人间":
                rt_no=5;
                break;
            case "豪华三人间":
                rt_no=6;
                break;
            case "总统套房":
                rt_no=7;
                break;
        }
        DBUtil db=new DBUtil();
        db.Connection();
        db.addRoom(rno, rt_no, rtel);
        jDialog2.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

